class CDN(nn.Module):

    def __init__(self, d_model=512, nhead=8, num_encoder_layers=6,
                 num_dec_layers_hopd=3, num_dec_layers_interaction=3, 
                 num_dec_layers_temporal=3, num_ref_frames=3,
                 dim_feedforward=2048, dropout=0.1,
                 activation="relu", normalize_before=False,
                 return_intermediate_dec=False, args=None, matcher=None):
        super().__init__()

        encoder_layer = TransformerEncoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        encoder_norm = nn.LayerNorm(d_model) if normalize_before else None
        self.encoder = TransformerEncoder(encoder_layer, num_encoder_layers, encoder_norm)

        decoder_layer = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        decoder_norm = nn.LayerNorm(d_model)
        self.decoder = TransformerDecoder(decoder_layer, num_dec_layers_hopd, decoder_norm,
                                          return_intermediate=return_intermediate_dec)

        interaction_decoder_layer = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        interaction_decoder_norm = nn.LayerNorm(d_model)
        self.interaction_decoder = TransformerDecoder(interaction_decoder_layer, num_dec_layers_interaction, 
                                                      interaction_decoder_norm, return_intermediate=return_intermediate_dec)
        # temporal modules
        self.ref_decoder = TransformerDecoder(interaction_decoder_layer, num_dec_layers_temporal, nn.LayerNorm(d_model))
        corre_decoder_layer = CorrelationDecoderLayer(d_model, nhead, dim_feedforward, dropout, activation, normalize_before)
        gated_corre_decoder_layer = GatedCorrelationDecoderLayer(d_model, nhead, dim_feedforward, dropout, activation, normalize_before)
        self.corre_decoder_cur = TransformerDecoder(corre_decoder_layer, num_dec_layers_temporal)
        self.corre_decoder_cur2 = TransformerDecoder(corre_decoder_layer, num_dec_layers_temporal)
        self.gated_corre_decoder_ref = TransformerDecoder(gated_corre_decoder_layer, num_dec_layers_temporal)
        self.gated_corre_decoder_cur = TransformerDecoder(gated_corre_decoder_layer, num_dec_layers_temporal)

        self._reset_parameters()

        self.d_model = d_model
        self.nhead = nhead
        self.num_ref_frames = num_ref_frames

    def _reset_parameters(self):
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

    def forward(self, src, mask, query_embed, pos_embed, targets=None, cur_idx=0):

        # >>>>> Image feature encoder <<<<<
        n, c, h, w = src.shape
        src = src.flatten(2).permute(2, 0, 1)
        pos_embed = pos_embed.flatten(2).permute(2, 0, 1)
        st_pos_embed = pos_embed
        query_embed = query_embed.unsqueeze(1).repeat(1, n, 1)
        mask = mask.flatten(1)

        tgt = torch.zeros_like(query_embed)
        memory = self.encoder(src, src_key_padding_mask=mask, pos=pos_embed)

        ref_ids = torch.as_tensor((list(range(cur_idx)) + list(range(cur_idx+1, n))), dtype=torch.long, device=memory.device)
        cur_memory = memory[:, cur_idx:cur_idx+1]
        cur_mask = mask[cur_idx:cur_idx+1]
        cur_pos_embed = pos_embed[:, cur_idx:cur_idx+1]
        cur_query_embed = query_embed[:, cur_idx:cur_idx+1]
        ref_memory = memory[:, ref_ids]
        ref_mask = mask[ref_ids]
        ref_pos_embed = pos_embed[:, ref_ids]
        ref_query_embed = query_embed[:, ref_ids]
        
        # >>>>> Spatial Transition Awareness for reference frames <<<<<
        temp_ref_memory_list = []
        for i in range(n - 1):
            temp_ref_memory_i = self.gated_corre_decoder_ref(ref_memory[:, i:i+1], cur_memory, tgt_key_padding_mask=ref_mask[i:i+1],
                                                             memory_key_padding_mask=cur_mask)
            temp_ref_memory_list.append(temp_ref_memory_i)
        temp_ref_memory = torch.cat(temp_ref_memory_list, dim=1)

        # >>>>> Use reference frames to reinforce current frame <<<<<
        flatten_ref_memory = ref_memory.transpose(0, 1).reshape(-1, 1, self.d_model)
        flatten_ref_mask = ref_mask.flatten()[None, :]
        temp_cur_memory = self.corre_decoder_cur(cur_memory, flatten_ref_memory,
                        tgt_key_padding_mask=cur_mask, memory_key_padding_mask=flatten_ref_mask)

        # >>>>> Progressive aggregation for current frame <<<<<
        flatten_temp_ref_memroy = temp_ref_memory.transpose(0, 1).reshape(-1, 1, self.d_model)
        temp2_cur_memory = self.corre_decoder_cur2(temp_cur_memory, flatten_temp_ref_memroy,
                        tgt_key_padding_mask=cur_mask, memory_key_padding_mask=flatten_ref_mask)
        
        # >>>>> Gated residual connection of current frame <<<<<
        final_cur_memory = self.gated_corre_decoder_cur(temp2_cur_memory, cur_memory,
                        tgt_key_padding_mask=cur_mask, memory_key_padding_mask=cur_mask)
        
        # >>>>> Introduce decoded queries from reference frames <<<<<
        ref_tgt = torch.zeros_like(ref_query_embed)
        ref_hopd_out = self.ref_decoder(ref_tgt, ref_memory, memory_key_padding_mask=ref_mask, pos=ref_pos_embed, query_pos=ref_query_embed)
        ref_hopd_out = ref_hopd_out.transpose(0, 1)

        # >>>>> Decode concatanated queries on current frame <<<<<
        # --- pair instance decoder ---
        ref_ins_hs = ref_hopd_out.flatten(0, 1)[None, :, :]
        ref_ins_hs = ref_ins_hs.transpose(0, 1)
        concat_cur_query_embed = torch.cat((ref_ins_hs, cur_query_embed), dim=0)
        cur_tgt = torch.zeros_like(concat_cur_query_embed)
        cur_hopd_out = self.decoder(cur_tgt, final_cur_memory, memory_key_padding_mask=cur_mask, pos=cur_pos_embed, query_pos=concat_cur_query_embed)
        cur_hopd_out = cur_hopd_out.transpose(1, 2)

        # --- relation decoder ---
        cur_interaction_query_embed = cur_hopd_out[-1]
        cur_interaction_query_embed = cur_interaction_query_embed.permute(1, 0, 2)
        cur_interaction_tgt = torch.zeros_like(cur_interaction_query_embed)
        cur_interaction_decoder_out = self.interaction_decoder(cur_interaction_tgt, final_cur_memory, memory_key_padding_mask=cur_mask,
                                        pos=cur_pos_embed, query_pos=cur_interaction_query_embed)
        cur_interaction_decoder_out = cur_interaction_decoder_out.transpose(1, 2)

        return cur_hopd_out, cur_interaction_decoder_out


class TransformerEncoder(nn.Module):

    def __init__(self, encoder_layer, num_layers, norm=None):
        super().__init__()
        self.layers = _get_clones(encoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm

    def forward(self, src,
                mask: Optional[Tensor] = None,
                src_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None):
        output = src

        for layer in self.layers:
            output = layer(output, src_mask=mask,
                           src_key_padding_mask=src_key_padding_mask, pos=pos)

        if self.norm is not None:
            output = self.norm(output)

        return output


class TransformerDecoder(nn.Module):

    def __init__(self, decoder_layer, num_layers, norm=None, return_intermediate=False):
        super().__init__()
        self.layers = _get_clones(decoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm
        self.return_intermediate = return_intermediate

    def forward(self, tgt, memory,
                tgt_mask: Optional[Tensor] = None,
                memory_mask: Optional[Tensor] = None,
                tgt_key_padding_mask: Optional[Tensor] = None,
                memory_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None,
                query_pos: Optional[Tensor] = None):
        output = tgt

        intermediate = []

        for layer in self.layers:
            output = layer(output, memory, tgt_mask=tgt_mask,
                           memory_mask=memory_mask,
                           tgt_key_padding_mask=tgt_key_padding_mask,
                           memory_key_padding_mask=memory_key_padding_mask,
                           pos=pos, query_pos=query_pos)
            if self.return_intermediate:
                intermediate.append(self.norm(output))

        if self.norm is not None:
            output = self.norm(output)
            if self.return_intermediate:
                intermediate.pop()
                intermediate.append(output)

        if self.return_intermediate:
            return torch.stack(intermediate)

        return output


class TransformerEncoderLayer(nn.Module):

    def __init__(self, d_model, nhead, dim_feedforward=2048, dropout=0.1,
                 activation="relu", normalize_before=False):
        super().__init__()
        self.self_attn = nn.MultiheadAttention(d_model, nhead, dropout=dropout)
        self.linear1 = nn.Linear(d_model, dim_feedforward)
        self.dropout = nn.Dropout(dropout)
        self.linear2 = nn.Linear(dim_feedforward, d_model)

        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        self.dropout1 = nn.Dropout(dropout)
        self.dropout2 = nn.Dropout(dropout)

        self.activation = _get_activation_fn(activation)
        self.normalize_before = normalize_before

    def with_pos_embed(self, tensor, pos: Optional[Tensor]):
        return tensor if pos is None else tensor + pos

    def forward_post(self,
                     src,
                     src_mask: Optional[Tensor] = None,
                     src_key_padding_mask: Optional[Tensor] = None,
                     pos: Optional[Tensor] = None):
        q = k = self.with_pos_embed(src, pos)
        src2 = self.self_attn(q, k, value=src, attn_mask=src_mask,
                              key_padding_mask=src_key_padding_mask)[0]
        src = src + self.dropout1(src2)
        src = self.norm1(src)
        src2 = self.linear2(self.dropout(self.activation(self.linear1(src))))
        src = src + self.dropout2(src2)
        src = self.norm2(src)
        return src

    def forward_pre(self, src,
                    src_mask: Optional[Tensor] = None,
                    src_key_padding_mask: Optional[Tensor] = None,
                    pos: Optional[Tensor] = None):
        src2 = self.norm1(src)
        q = k = self.with_pos_embed(src2, pos)
        src2 = self.self_attn(q, k, value=src2, attn_mask=src_mask,
                              key_padding_mask=src_key_padding_mask)[0]
        src = src + self.dropout1(src2)
        src2 = self.norm2(src)
        src2 = self.linear2(self.dropout(self.activation(self.linear1(src2))))
        src = src + self.dropout2(src2)
        return src

    def forward(self, src,
                src_mask: Optional[Tensor] = None,
                src_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None):
        if self.normalize_before:
            return self.forward_pre(src, src_mask, src_key_padding_mask, pos)
        return self.forward_post(src, src_mask, src_key_padding_mask, pos)


