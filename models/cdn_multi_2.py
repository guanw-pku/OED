class Gate(nn.Module):
    def __init__(self, channel):
        super().__init__()
        self.sigmoid = nn.Sigmoid()
        self.w = nn.Linear(channel*2, channel)

    def forward(self, q, v):
        qv = torch.cat((q,v), dim=-1)  # (..., 2*channel)

        z = self.sigmoid(self.w(qv))
        h = (1 - z) * v + z * q
        
        return h


def _get_clones(module, N):
    return nn.ModuleList([copy.deepcopy(module) for i in range(N)])


def build_cdn(args, matcher):
    return CDN(
        d_model=args.hidden_dim,
        dropout=args.dropout,
        nhead=args.nheads,
        dim_feedforward=args.dim_feedforward,
        num_encoder_layers=args.enc_layers,
        num_dec_layers_hopd=args.dec_layers_hopd,
        num_dec_layers_interaction=args.dec_layers_interaction,
        num_dec_layers_temporal=args.dec_layers_temporal,
        num_ref_frames=args.num_ref_frames,
        normalize_before=args.pre_norm,
        return_intermediate_dec=True,
        args=args,
        matcher=matcher
    )


def _get_activation_fn(activation):
    if activation == "relu":
        return F.relu
    if activation == "gelu":
        return F.gelu
    if activation == "glu":
        return F.glu
    raise RuntimeError(F"activation should be relu/gelu, not {activation}.")
